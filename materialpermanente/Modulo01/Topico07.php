                            <h2 class="Titulo">Tópico 07 - LEGISLAÇÃO PATRIMONIAL:</h2>

                            <br>
                            
                            <p class="Texto"><strong>Objetivo:</strong> Compreender a legislação patrimonial no âmbito federal e estadual apresentado os dispositivos que interferem direta e indiretamente na gestão do material permanente.</p>

                            <!-- Sub Título -->
                            <p class="SubTitulo">07.1 - Legislação Federal:</p>
                            <hr>

                            <!-- Sub Sub Título -->
                            <p class="SubSubTitulo">07.1.1 - Lei nº 4.320 de 17 de março de 1964: </p>
                            <hr>

                            <p class="Texto">Estatui Normas Gerais de Direito Financeiro para elaboração e controle dos orçamentos e balanços da União, dos Estados, dos Municípios e do Distrito Federal.
                            <br><br><h4><center><strong>CAPÍTULO III<br> Da Despesa</strong></center></h4></p>
                            <hr>

                            <p class="Texto"><strong>Art. 15.</strong> Na Lei de Orçamento a discriminação da despesa far-se-á no mínimo por elementos.</p>

                            <ul class="Texto">
                                <li><strong>§ 1º</strong> Entende-se por elementos o desdobramento da despesa com pessoal, <strong>material</strong>, serviços, obras e outros meios de que se serve a administração pública para consecução dos seus fins.</li>
                                <li><strong>§ 2º</strong> Para efeito de classificação da despesa, considera-se <strong>material permanente</strong> o de duração superior a dois anos.</li>
                            </ul>

                            <p class="Texto"><h4><strong><center>CAPÍTULO III<br>Da Contabilidade Patrimonial e Industrial</center></strong></h4></p><hr>

                            <ul class="Texto">
                                <li><strong>Art. 94.</strong> Haverá registros analíticos de todos os <strong>bens de caráter permanente</strong>, com indicação dos elementos necessários para a perfeita caracterização de cada um deles e dos agentes responsáveis pela sua guarda e administração.</li>
                                <li><strong>Art. 96.</strong> O levantamento geral dos <strong>bens móveis</strong> e imóveis terá por base o <strong>inventário analítico</strong> de cada unidade administrativa e os elementos da escrituração sintética na contabilidade.</li>
                            </ul>

                            <p class="Texto"><h4><strong><center>CAPÍTULO IV<br>Dos Balanços</center></strong></h4> </p><hr>

                            <ul class="Texto">
                                <li><strong>Art. 104.</strong> A Demonstração das Variações Patrimoniais evidenciará as alterações verificadas no patrimônio, resultantes ou independentes da execução orçamentária, e indicará o resultado patrimonial do exercício.</li>
                                <li><strong>Art. 106.</strong> A avaliação dos elementos patrimoniais obedecerá às normas seguintes:</li>
                                    <ul>
                                        <li>os débitos e créditos, bem como os títulos de renda, pelo seu valor nominal, feita a conversão, quando em moeda estrangeira, à taxa de câmbio vigente na data do balanço;</li>
                                        <li>os bens móveis e imóveis, pelo valor de aquisição ou pelo custo de produção ou de construção;</li>
                                        <li>os bens de almoxarifado, pelo preço médio ponderado das compras.</li>
                                    </ul>

                                <li><strong>§ 1°</strong> Os valores em espécie, assim como os débitos e créditos, quando em moeda estrangeira, deverão figurar ao lado das correspondentes importâncias em moeda nacional.</li>
                                <li><strong>§ 2º</strong> As variações resultantes da conversão dos débitos, créditos e valores em espécie serão levadas à conta patrimonial.</li>
                                <li><strong>§ 3º</strong> Poderão ser feitas reavaliações dos bens móveis e imóveis.</li>
                            </ul>

                            <!-- Sub Sub Título -->
                            <p class="SubSubTitulo">7.1.2) Lei nº 8.429 de 2 de junho de 1992. </p>
                            <hr>

                            <p class="Texto">Dispõe sobre as sanções aplicáveis aos agentes públicos nos casos de enriquecimento ilícito no exercício de mandato, cargo, emprego ou função na administração pública direta, indireta ou fundacional e dá outras providências. </p>

                            <p class="Texto"><h4><strong><center>CAPÍTULO I<br>Das Disposições Gerais</center></strong></h4> </p><hr>

                            <ul class="Texto">
                                <li><strong>Art. 1°</strong> Os atos de improbidade praticados por qualquer agente público, servidor ou não, contra a administração direta, indireta ou fundacional de qualquer dos Poderes da União, dos Estados, do Distrito Federal, dos Municípios, de Território, de empresa incorporada ao <strong>patrimônio público</strong> ou de entidade para cuja criação ou custeio o erário haja concorrido ou concorra com mais de cinquenta por cento do patrimônio ou da receita anual, serão punidos na forma desta lei.</li>
                                <li><strong>Parágrafo único.</strong> Estão também sujeitos às penalidades desta lei os atos de improbidade praticados contra o patrimônio de entidade que receba subvenção, benefício ou incentivo, fiscal ou creditício, de órgão público bem como daquelas para cuja criação ou custeio o erário haja concorrido ou concorra com menos de cinquenta por cento do patrimônio ou da receita anual, limitando-se, nestes casos, a sanção patrimonial à repercussão do ilícito sobre a contribuição dos cofres públicos.</li>
                                <li><strong>Art. 2°</strong> Reputa-se agente público, para os efeitos desta lei, todo aquele que exerce, ainda que transitoriamente ou sem remuneração, por eleição, nomeação, designação, contratação ou qualquer outra forma de investidura ou vínculo, mandato, cargo, emprego ou função nas entidades mencionadas no artigo anterior.</li>
                                <li><strong>Art. 3°</strong> As disposições desta lei são aplicáveis, no que couber, àquele que, mesmo não sendo agente público, induza ou concorra para a prática do ato de improbidade ou dele se beneficie sob qualquer forma direta ou indireta.</li>
                                <li><strong>Art. 5°</strong> Ocorrendo lesão ao patrimônio público por ação ou omissão, dolosa ou culposa, do agente ou de terceiro, dar-se-á o integral ressarcimento do dano.</li>
                            </ul>

                            <p class="Texto"><h4><strong><center>CAPÍTULO II<br>Dos Atos de Improbidade Administrativa<br><br><hr>Seção I<br>Dos Atos de Improbidade Administrativa que Importam Enriquecimento Ilícito</center></strong></h4> </p><hr>

                            <ul class="Texto">
                                <li><strong>Art. 9°</strong> Constitui ato de improbidade administrativa importando enriquecimento ilícito auferir qualquer tipo de vantagem patrimonial indevida em razão do exercício de cargo, mandato, função, emprego ou atividade nas entidades mencionadas no art. 1° desta lei, e notadamente:</li>
                                    <ol type="I">
                                        <li>Receber, para si ou para outrem, dinheiro, bem móvel ou imóvel, ou qualquer outra vantagem econômica, direta ou indireta, a título de comissão, percentagem, gratificação ou presente de quem tenha interesse, direto ou indireto, que possa ser atingido ou amparado por ação ou omissão decorrente das atribuições do agente público;</li>
                                        <li>Perceber vantagem econômica, direta ou indireta, para facilitar a aquisição, permuta ou locação de bem móvel ou imóvel, ou a contratação de serviços pelas entidades referidas no art. 1° por preço superior ao valor de mercado;</li>
                                        <li>Perceber vantagem econômica, direta ou indireta, para facilitar a alienação, permuta ou locação de bem público ou o fornecimento de serviço por ente estatal por preço inferior ao valor de mercado;</li>
                                        <li>Utilizar, em obra ou serviço particular, veículos, máquinas, equipamentos ou material de qualquer natureza, de propriedade ou à disposição de qualquer das entidades mencionadas no art. 1° desta lei, bem como o trabalho de servidores públicos, empregados ou terceiros contratados por essas entidades;</li>
                                        <li>Usar, em proveito próprio, bens, rendas, verbas ou valores integrantes do acervo patrimonial das entidades mencionadas no art. 1° desta lei.</li>
                                    </ol>
                            </ul>

