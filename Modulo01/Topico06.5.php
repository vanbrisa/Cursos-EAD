                            <h2 class="Titulo">6.5) Movimentação de Bens Móveis:</h2>

                            <br>
                            
                            <p class="Texto">São os procedimentos relativos à distribuição, remanejamento, saída provisória, cessão, transferência e doação a que estão sujeitos os bens móveis, no período decorrido entre sua incorporação e desincorporação. Compete à Gestão Setorial de Patrimônio dos Órgãos e Entidades a operacionalização das movimentações dos bens móveis.</p>

                            <!-- Sub Sub Título -->
                            <p class="SubSubTitulo">6.5.1 - Distribuição de Bens:</p>
                            <hr>

                            <p class="Texto">O bem depois de tombado e cadastrado está pronto para ser distribuído para uma unidade dentro do órgão, visando atender as necessidades dela. É nesse momento que se inicia o controle do bem. O documento que viabiliza este procedimento é o Termo de Responsabilidade, no qual um bem é posto sob a guarda, conservação e controle do agente responsável, titular da unidade administrativa na qual o bem está localizado, mediante sua assinatura. A função do Termo de Responsabilidade é identificar os bens por unidade dentro de um órgão, bem como o servidor responsável por prestar informações sobre os mesmos perante a unidade de patrimônio do órgão, tais como: danificação, inservibilidade, necessidade de reparo, desaparecimento, etc.
                            <br><br> Os bens de terceiros provenientes de cessão e locação deverão ser relacionados no Termo de Responsabilidade, e conferidos periodicamente. Em caso de desaparecimento deles, o órgão poderá ser penalizado de acordo com o previsto no respectivo Termo de Cessão, Contrato em Comodato ou Contrato de Locação. Os bens cuja constituição física não permita que seja afixada etiqueta de identificação, também serão relacionados no Termo de Responsabilidade. 
                            <br><br>Dentre as informações mais importantes que deverão ser incluídas no Termo de Responsabilidade, conforme anexo III do Decreto nº 27.786 de 02 de maio de 2005 destacam-se:</p>

                            <ul class="Texto">
                                <i class="fa fa-check-circle CorVerdeClaroEstado"></i> Órgão/Entidade;</li>
                                <br><i class="fa fa-check-circle CorVerdeClaroEstado"></i> Unidade Administrativa onde o bem está localizado;</li>
                                <br><i class="fa fa-check-circle CorVerdeClaroEstado"></i> Alterações: nº do Tombamento do Bem, especificação do bem, motivo;</li>
                                <br><i class="fa fa-check-circle CorVerdeClaroEstado"></i> Estado de conservação;</li>
                                <br><i class="fa fa-check-circle CorVerdeClaroEstado"></i> Data e assinatura do responsável da unidade administrativa onde o bem está localizado.</li>
                            </ul>

                            <p class="Texto">O Termo de Responsabilidade deverá ser emitido em duas vias ficando uma com o agente responsável e outra com a unidade de patrimônio. Esse termo deverá ser atualizado quando houver troca do agente responsável, movimentação e baixa de um bem.
                            <br><br>A seguir relacionamos as competências do signatário do Termo de Responsabilidade: </p>

                            <ul>
                                <i class="fa fa-check CorVerdeClaroEstado"></i> Assumir a responsabilidade pelos bens que estão destinados à seu uso ou do setor que está sob sua chefia ou direção.
                                <br><i class="fa fa-check CorVerdeClaroEstado"></i> Comunicar à Gerência de Patrimônio qualquer movimentação dos bens sob a sua responsabilidade.
                                <br><i class="fa fa-check CorVerdeClaroEstado"></i> Comunicar, imediatamente, a quem de direito, qualquer irregularidade ocorrida com o material entregue aos seus cuidados
                                <br><i class="fa fa-check CorVerdeClaroEstado"></i> Nos impedimentos legais temporários (férias, licenças, afastamentos, e outros), informar de forma expressa e escrita o nome do seu substituto para que a ele seja atribuída à responsabilidade provisória pela guarda do bem.
                                <br><i class="fa fa-check CorVerdeClaroEstado"></i> Todo servidor ao ser desvinculado do cargo, função ou emprego, deverá passar a responsabilidade do material sob sua guarda a outrem, comunicando expressamente ao gestor patrimonial, salvo em caso de força maior, quando:
                                    <ol>
                                        <li>Impossibilitado de fazer, pessoalmente, a passagem de responsabilidade do material, poderá o servidor delegar a terceiros essa incumbência; ou</li>
                                        <li>Não tendo esse procedido na forma da alínea anterior, poderá ser designado servidor do órgão, ou instituída Comissão Especial pelo dirigente do órgão e entidade, nos casos de carga mais vultosa, para conferência e passagem do material.</li>
                                    </ol>
                                <br><i class="fa fa-check CorVerdeClaroEstado"></i> Caberá ao órgão cujo servidor estiver deixando o cargo, função ou emprego, tomar as providências preliminares para a passagem de responsabilidade, indicando, inclusive, o nome de seu substituto ao setor de controle do material permanente.
                                <br><i class="fa fa-check CorVerdeClaroEstado"></i> A passagem de responsabilidade deverá ser feita obrigatoriamente, à vista da verificação física de cada material permanente e lavratura de novo Termo de Responsabilidade.
                            <ul>

                            <!-- Sub Sub Título -->
                            <p class="SubSubTitulo">6.5.2) Transferência de bens: </p>
                            <hr>

                            <p class="Texto">A movimentação por transferência caracteriza-se pelo deslocamento definitivo do bem entre órgãos da Administração Direta. Neste caso, o documento a ser utilizado é o Termo de Transferência. Este deverá ser emitido e assinado entre os contratantes.
                            <br><br>Nenhum bem poderá ser transferido sem emissão de termo formalizando a mudança de responsável pela posse e propriedade do bem. É importante lembrar que o transmitente, além da necessidade da atualização do Termo de Responsabilidade, é necessário efetuar a baixa no seu registro patrimonial, após celebrada a transferência. Outra questão importante a ser observada é a verificação do estado de conservação do bem e se seu valor contábil líquido é condizente com a condição que o bem se encontra, caso contrário faz-se necessário reavaliar o bem.
                            </p>

                            <!-- Sub Sub Título -->
                            <p class="SubSubTitulo">6.5.3) Doação de Bens: </p>
                            <hr>

                            <p class="Texto">Todos os processos de doação do patrimônio público devem ser intermediados órgão de controle central do patrimônio. O processo de doação a ser encaminhado para a interveniência do referido órgão deve estar instruído como os seguintes documentos:

                            <br><br> <i class="fa fa-check CorVerdeClaroEstado"></i> <strong>Doações para entidades privadas:</strong>
                            </p>

                            <ul>
                                <i class="fa fa-check-circle CorVerdeClaroEstado"></i> Declaração de Utilidade Pública, por intermédio de lei municipal, estadual ou federal:
                                <br><i class="fa fa-check-circle CorVerdeClaroEstado"></i> Cópia do Estatuto ou Ato Constitutivo em vigor;
                                <br><i class="fa fa-check-circle CorVerdeClaroEstado"></i> Ata da eleição de posse dos cargos superiores de deliberação e administração que estejam em exercício;
                                <br><i class="fa fa-check-circle CorVerdeClaroEstado"></i> Cópia do CNPJ;
                                <br><i class="fa fa-check-circle CorVerdeClaroEstado"></i> Certidões negativas ou isenção com relação a tributos;
                                <br><i class="fa fa-check-circle CorVerdeClaroEstado"></i> Minuta do Decreto e Termo de Doação fundamentados na legislação vigente;
                                <br><i class="fa fa-check-circle CorVerdeClaroEstado"></i> Relação de Bens a serem doados.
                            </ul>

                            <p class="Texto"><br><br> <i class="fa fa-check CorVerdeClaroEstado"></i> <strong>Doações para Administração Indireta: </strong>
                            </p>

                            <ul>
                                <i class="fa fa-check-circle CorVerdeClaroEstado"></i> Minuta do Termo de Doação evidenciando a doação como exclusivamente para fins e uso de interesse social, após avaliação de sua oportunidade e conveniência sócio-econômica, relativamente à escolha de outra forma de alienação.
                                <i class="fa fa-check-circle CorVerdeClaroEstado"></i> Relação de Bens para Doação com a especificação e valor.
                            </ul>

                            <p class="Texto"><br><br> <i class="fa fa-check CorVerdeClaroEstado"></i> <strong>6.5.4) Cessão de Uso:  </strong>
                            </p>

                            <p class="Texto">A Cessão de Uso é a movimentação externa de material, com transferência gratuita de posse e troca de responsabilidade, de caráter temporário, entre órgãos ou entidades da Administração Pública Direta, Autárquica e Fundacional do Poder Executivo, ou entre estes e órgãos de quaisquer dos Poderes, do Ministério Público, do Tribunal de Contas ou de outra esfera da Federação.
                            <br><br>É competência de Secretário Estadual ou dirigente máximo de órgão autônomo, autarquia e fundação, entregar e aceitar bem em cessão de uso.
                            <br><br>O processo de cessão de uso a ser celebrado entre as partes, publicado em Diário Oficial e deve estar instruído como os seguintes documentos: </p>

                            <ul class="Texto">
                                <i class="fa fa-checl-circle CorVerdeClaroEstado"></i> Ofício elaborado pelo interessado na cessão, devidamente justificado, solicitando a posse do bem e a sua destinação; 
                                <br><i class="fa fa-checl-circle CorVerdeClaroEstado"></i> Termo de cessão de uso assinado pelo Secretário, dirigente máximo de órgão autônomo, entidade autárquica e fundacional do Poder Executivo, na figura do cedente, no qual o bem esteja incorporado e pelo representante legal do órgão ou entidade destinatário do bem na qualidade de cessionário; e 
                                <br><i class="fa fa-checl-circle CorVerdeClaroEstado"></i> Parecer técnico do órgão ou entidade cedente, motivando a assinatura do termo.
                            </ul>


                                
                            


                            
          
