                            <h2 class="Titulo">Tópico 07 - LEGISLAÇÃO PATRIMONIAL:</h2>

                            <br>
                            
                            <p class="Texto"><strong>Objetivo:</strong> Compreender a legislação patrimonial no âmbito federal e estadual apresentado os dispositivos que interferem direta e indiretamente na gestão do material permanente.</p>

                            <!-- Sub Título -->
                            <p class="SubTitulo">07.1 - Legislação Federal:</p>
                            <hr>

                            <!-- Sub Sub Título -->
                            <p class="SubSubTitulo">07.1.1 - Lei nº 4.320 de 17 de março de 1964: </p>
                            <hr>

                            <p class="Texto">Estatui Normas Gerais de Direito Financeiro para elaboração e controle dos orçamentos e balanços da União, dos Estados, dos Municípios e do Distrito Federal.
                            <br><br><h4><center><strong>CAPÍTULO III<br> Da Despesa</strong></center></h4></p>
                            <hr>

                            <p class="Texto"><strong>Art. 15.</strong> Na Lei de Orçamento a discriminação da despesa far-se-á no mínimo por elementos.</p>

                            <ul class="Texto">
                                <li><strong>§ 1º</strong> Entende-se por elementos o desdobramento da despesa com pessoal, <strong>material</strong>, serviços, obras e outros meios de que se serve a administração pública para consecução dos seus fins.</li>
                                <li><strong>§ 2º</strong> Para efeito de classificação da despesa, considera-se <strong>material permanente</strong> o de duração superior a dois anos.</li>
                            </ul>

                            <p class="Texto"><h4><strong><center>CAPÍTULO III<br>Da Contabilidade Patrimonial e Industrial</center></strong></h4></p><hr>

                            <ul class="Texto">
                                <li><strong>Art. 94.</strong> Haverá registros analíticos de todos os <strong>bens de caráter permanente</strong>, com indicação dos elementos necessários para a perfeita caracterização de cada um deles e dos agentes responsáveis pela sua guarda e administração.</li>
                                <li><strong>Art. 96.</strong> O levantamento geral dos <strong>bens móveis</strong> e imóveis terá por base o <strong>inventário analítico</strong> de cada unidade administrativa e os elementos da escrituração sintética na contabilidade.</li>
                            </ul>

                            <p class="Texto"><h4><strong><center>CAPÍTULO IV<br>Dos Balanços</center></strong></h4> </p><hr>

                            <ul class="Texto">
                                <li><strong>Art. 104.</strong> A Demonstração das Variações Patrimoniais evidenciará as alterações verificadas no patrimônio, resultantes ou independentes da execução orçamentária, e indicará o resultado patrimonial do exercício.</li>
                                <li><strong>Art. 106.</strong> A avaliação dos elementos patrimoniais obedecerá às normas seguintes:</li>
                                    <ul>
                                        <li>os débitos e créditos, bem como os títulos de renda, pelo seu valor nominal, feita a conversão, quando em moeda estrangeira, à taxa de câmbio vigente na data do balanço;</li>
                                        <li>os bens móveis e imóveis, pelo valor de aquisição ou pelo custo de produção ou de construção;</li>
                                        <li>os bens de almoxarifado, pelo preço médio ponderado das compras.</li>
                                    </ul>

                                <li><strong>§ 1°</strong> Os valores em espécie, assim como os débitos e créditos, quando em moeda estrangeira, deverão figurar ao lado das correspondentes importâncias em moeda nacional.</li>
                                <li><strong>§ 2º</strong> As variações resultantes da conversão dos débitos, créditos e valores em espécie serão levadas à conta patrimonial.</li>
                                <li><strong>§ 3º</strong> Poderão ser feitas reavaliações dos bens móveis e imóveis.</li>
                            </ul>

                            <!-- Sub Sub Título -->
                            <p class="SubSubTitulo">7.1.2) Lei nº 8.429 de 2 de junho de 1992. </p>
                            <hr>

                            <p class="Texto">Dispõe sobre as sanções aplicáveis aos agentes públicos nos casos de enriquecimento ilícito no exercício de mandato, cargo, emprego ou função na administração pública direta, indireta ou fundacional e dá outras providências. </p>

                            <p class="Texto"><h4><strong><center>CAPÍTULO I<br>Das Disposições Gerais</center></strong></h4> </p><hr>

                            <ul class="Texto">
                                <li><strong>Art. 1°</strong> Os atos de improbidade praticados por qualquer agente público, servidor ou não, contra a administração direta, indireta ou fundacional de qualquer dos Poderes da União, dos Estados, do Distrito Federal, dos Municípios, de Território, de empresa incorporada ao <strong>patrimônio público</strong> ou de entidade para cuja criação ou custeio o erário haja concorrido ou concorra com mais de cinquenta por cento do patrimônio ou da receita anual, serão punidos na forma desta lei.</li>
                                <li><strong>Parágrafo único.</strong> Estão também sujeitos às penalidades desta lei os atos de improbidade praticados contra o patrimônio de entidade que receba subvenção, benefício ou incentivo, fiscal ou creditício, de órgão público bem como daquelas para cuja criação ou custeio o erário haja concorrido ou concorra com menos de cinquenta por cento do patrimônio ou da receita anual, limitando-se, nestes casos, a sanção patrimonial à repercussão do ilícito sobre a contribuição dos cofres públicos.</li>
                                <li><strong>Art. 2°</strong> Reputa-se agente público, para os efeitos desta lei, todo aquele que exerce, ainda que transitoriamente ou sem remuneração, por eleição, nomeação, designação, contratação ou qualquer outra forma de investidura ou vínculo, mandato, cargo, emprego ou função nas entidades mencionadas no artigo anterior.</li>
                                <li><strong>Art. 3°</strong> As disposições desta lei são aplicáveis, no que couber, àquele que, mesmo não sendo agente público, induza ou concorra para a prática do ato de improbidade ou dele se beneficie sob qualquer forma direta ou indireta.</li>
                                <li><strong>Art. 5°</strong> Ocorrendo lesão ao patrimônio público por ação ou omissão, dolosa ou culposa, do agente ou de terceiro, dar-se-á o integral ressarcimento do dano.</li>
                            </ul>

                            <p class="Texto"><h4><strong><center>CAPÍTULO II<br>Dos Atos de Improbidade Administrativa<br><br><hr>Seção I<br>Dos Atos de Improbidade Administrativa que Importam Enriquecimento Ilícito</center></strong></h4> </p><hr>

                            <ul class="Texto">
                                <li><strong>Art. 9°</strong> Constitui ato de improbidade administrativa importando enriquecimento ilícito auferir qualquer tipo de vantagem patrimonial indevida em razão do exercício de cargo, mandato, função, emprego ou atividade nas entidades mencionadas no art. 1° desta lei, e notadamente:</li>
                                    <ol type="I">
                                        <li>Receber, para si ou para outrem, dinheiro, bem móvel ou imóvel, ou qualquer outra vantagem econômica, direta ou indireta, a título de comissão, percentagem, gratificação ou presente de quem tenha interesse, direto ou indireto, que possa ser atingido ou amparado por ação ou omissão decorrente das atribuições do agente público;</li>
                                        <li>Perceber vantagem econômica, direta ou indireta, para facilitar a aquisição, permuta ou locação de bem móvel ou imóvel, ou a contratação de serviços pelas entidades referidas no art. 1° por preço superior ao valor de mercado;</li>
                                        <li>Perceber vantagem econômica, direta ou indireta, para facilitar a alienação, permuta ou locação de bem público ou o fornecimento de serviço por ente estatal por preço inferior ao valor de mercado;</li>
                                        <li>Utilizar, em obra ou serviço particular, veículos, máquinas, equipamentos ou material de qualquer natureza, de propriedade ou à disposição de qualquer das entidades mencionadas no art. 1° desta lei, bem como o trabalho de servidores públicos, empregados ou terceiros contratados por essas entidades;</li>
                                        <li>Usar, em proveito próprio, bens, rendas, verbas ou valores integrantes do acervo patrimonial das entidades mencionadas no art. 1° desta lei.</li>
                                    </ol>
                            </ul>

                            <br><p class="Texto"><h4><strong><center>Seção II<br>Dos Atos de Improbidade Administrativa que Causam Prejuízo ao Erário</center></strong></h4> </p><hr>

                            <ul class="Texto">
                                <li><strong>Art. 10. Constitui ato de improbidade administrativa que causa lesão ao erário qualquer ação ou omissão, dolosa ou culposa, que enseje perda patrimonial, desvio, apropriação, malbaratamento ou dilapidação dos bens ou haveres das entidades referidas no art. 1º desta lei, e notadamente:</li>
                                    <ol type="I">
                                        <li>Facilitar ou concorrer por qualquer forma para a incorporação ao patrimônio particular, de pessoa física ou jurídica, de bens, rendas, verbas ou valores integrantes do acervo patrimonial das entidades mencionadas no art. 1º desta lei;</li>
                                        <li>Permitir ou concorrer para que pessoa física ou jurídica privada utilize bens, rendas, verbas ou valores integrantes do acervo patrimonial das entidades mencionadas no art. 1º desta lei, sem a observância das formalidades legais ou regulamentares aplicáveis à espécie;</li>
                                        <li>Doar à pessoa física ou jurídica bem como ao ente despersonalizado, ainda que de fins educativos ou assistências, bens, rendas, verbas ou valores do patrimônio de qualquer das entidades mencionadas no art. 1º desta lei, sem observância das formalidades legais e regulamentares aplicáveis à espécie;</li>
                                        <li>Permitir ou facilitar a alienação, permuta ou locação de bem integrante do patrimônio de qualquer das entidades referidas no art. 1º desta lei, ou ainda a prestação de serviço por parte delas, por preço inferior ao de mercado;</li>
                                        <li>Agir negligentemente na arrecadação de tributo ou renda, bem como no que diz respeito à conservação do patrimônio público;</li>
                                    </ol>
                            </ul>

                            <!-- Sub Sub Título -->
                            <p class="SubSubTitulo">7.1.3) Lei nº 8.666 de 21 de junho de 1993. </p>
                            <hr>

                            <p>Regulamenta o art. 37, inciso XXI, da Constituição Federal, institui normas para licitações e contratos da Administração Pública e dá outras providências. </p>

                            <br><p class="Texto"><h4><strong><center>Capítulo I<br>DAS DISPOSIÇÕES GERAIS<br>Seção I<br>Dos Princípios</center></strong></h4> </p><hr>

                            <ol type="I">
                                <li>Art. 1º Esta lei estabelece normas gerais sobre licitações e contratos administrativos pertinentes a obras, serviços, inclusive de publicidade, compras, alienações e locações no âmbito dos Poderes da União, dos Estados, do Distrito Federal e dos Municípios.</li>
                                <li>Parágrafo único. Subordinam-se ao regime desta lei, além dos órgãos da administração direta, os fundos especiais, as autarquias, as fundações públicas, as empresas públicas, as sociedades de economia mista e demais entidades controladas direta ou indiretamente pela União, Estados, Distrito Federal e Municípios.</li>
                                <li>Art. 2º As obras, serviços, inclusive de publicidade, compras, alienações, concessões, permissões e locações da Administração Pública, quando contratadas com terceiros, serão necessariamente precedidas de licitação, ressalvadas as hipóteses previstas nesta lei.</li>
                            </ol>

                            <br><p class="Texto"><h4><strong><center>Seção VI<br>Das Alienações</center></strong></h4> </p><hr>

                            <ul class="Texto">
                                <li><strong>Art. 17. A alienação de bens da Administração Pública, subordinada à existência de interesse público devidamente justificado, será precedida de avaliação e obedecerá às seguintes normas:</li>
                                <li>II - quando móveis, dependerá de avaliação prévia e de licitação, dispensada esta nos seguintes casos:</li>
                                    <ol type="I">
                                        <li>doação, permitida exclusivamente para fins e uso de interesse social, após avaliação de sua oportunidade e conveniência sócio-econômica, relativamente à escolha de outra forma de alienação;</li>
                                        <li>permuta, permitida exclusivamente entre órgãos ou entidades da Administração Pública;</li>
                                        <li>venda de ações, que poderão ser negociadas em bolsa, observada a legislação específica;</li>
                                        <li>venda de títulos, na forma da legislação pertinente;</li>
                                        <li>venda de bens produzidos ou comercializados por órgãos ou entidades da Administração Pública, em virtude de suas finalidades;</li>
                                        <li>venda de materiais e equipamentos para outros órgãos ou entidades da Administração Pública, sem utilização previsível por quem deles dispõe.</li>
                                    </ol>
                            </ul>

                            <br><p class="Texto"><h4><strong><center>Capítulo II<br>Da Licitação<br>Seção I<br>Das Modalidades, Limites e Dispensa</center></strong></h4> </p><hr>

                            <ul class="Texto">
                                <li>Art. 22. São modalidades de licitação:</li>
                                    <ol type="I">
                                        <li>concorrência;</li>
                                        <li>tomada de preços;</li>
                                        <li>convite;</li>
                                        <li>concurso;</li>
                                        <li>leilão.</li>
                                    </ol>

                                    <li>§ 5º Leilão é a modalidade de licitação entre quaisquer interessados para a venda de bens móveis inservíveis para a Administração ou de produtos legalmente apreendidos ou penhorados, a quem oferecer o maior lance, igual ou superior ao da avaliação.</li>
                            </ul>

                            <!-- Sub Sub Título -->
                            <p class="SubSubTitulo">7.1.4) Portaria nº 448 STN de 13 de setembro de 2002.</p>
                            <hr>

                            <p>Divulga o detalhamento das naturezas de despesas 339030, 339036, 339039 e 449052. 
                            <br><br>O SECRETÁRIO DO TESOURO NACIONAL, no exercício das atribuições que lhe foram conferidas pela Portaria MF/GM nº 71, de 08 de abril de 1996, e;
                            <br><br>Considerando o disposto no parágrafo 2º, do artigo 50 da Lei Complementar nº 101, de 04 de maio de 2000, que atribui encargos ao órgão central de contabilidade da União;
                            <br><br>Considerando o contido no inciso I do artigo 4º do Decreto nº 3.589, de 06/09/2000, que confere à Secretaria do Tesouro Nacional do Ministério da Fazenda – STN/MF a condição de órgão central do Sistema de Contabilidade Federal;
                            <br><br>Considerando as competências do órgão central do Sistema de Contabilidade Federal, estabelecidas no artigo 5º do Decreto nº 3.589, de 06/09/2000, complementadas pela atribuição definida no inciso XVI, do artigo 8º do Decreto nº 3.366, de 26/02/2000 e conforme artigo 18 da Lei nº 10.180, de 06 de fevereiro de 2001;
                            <br><br>Considerando, ainda, a necessidade de desenvolver mecanismos que assegurem, de forma homogênea, a apropriação contábil de subitens de despesas para todas as esferas de governo, de forma a garantir a consolidação das contas exigida pela Lei de Responsabilidade Fiscal. </p>

                            <ul class="Texto">
                                <li>RESOLVE:</li>
                                <li>Art. 1º - Divulgar o detalhamento das naturezas de despesa, 339030 - Material de Consumo, 339036 – Outros Serviços de Terceiros Pessoa Física, 339039 - Outros Serviços de Terceiros Pessoa Jurídica e 449052 - Equipamentos e Material Permanente, de acordo com os anexos I, II, III, IV, respectivamente, para fins de utilização pela União, Estados, DF e Municípios, com o objetivo de auxiliar, em nível de execução, o processo de apropriação contábil da despesa que menciona.</li>
                                <li>Art. 2º - Para efeito desta Portaria, entende-se como material de consumo e material permanente:</li>
                                    <ol type="I">
                                        <li>I - Material de Consumo, aquele que, em razão de seu uso corrente e da definição da Lei n. 4.320/64, perde normalmente sua identidade física e/ou tem sua utilização limitada a dois anos;</li>
                                        <li>II - Material Permanente, aquele que, em razão de seu uso corrente, não perde a sua identidade física, e/ou tem uma durabilidade superior a dois anos.</li>
                                    </ol>
                                    <li>Art. 3º - Na classificação da despesa serão adotados os seguintes parâmetros excludentes, tomados em conjunto, para a identificação do material permanente:</li>
                                    <ol>
                                        <li>Durabilidade, quando o material em uso normal perde ou tem reduzidas as suas condições de funcionamento, no prazo máximo de dois anos;</li>
                                        <li>Fragilidade, cuja estrutura esteja sujeita a modificação, por ser quebradiço ou deformável, caracterizando-se pela irrecuperabilidade e/ou perda de sua identidade;</li>
                                        <li>Perecibilidade, quando sujeito a modificações (químicas ou físicas) ou que se deteriora ou perde sua característica normal de uso;</li>
                                        <li>Incorporabilidade, quando destinado à incorporação a outro bem, não podendo ser retirado sem prejuízo das características do principal; e</li>
                                        <li>Transformabilidade, quando adquirido para fim de transformação.</li>
                                    </ol>

                                    <li>Art. 5º - Os componentes relacionados esgotam todos os tipos de bens, materiais ou serviços possíveis de serem adquiridos ou contratados pelos órgãos, razão pela qual os executores deverão utilizar o grupo que mais se assemelha às características do item a ser apropriado, quando não expressamente citado. Por outro lado, o fato de um material ou serviço estar exemplificado ementário não significa que não possa ser classificado em outro do mesmo elemento de despesa, desde que possua outra aplicação específica.</li>
                                    <li>Art. 6º - A despesa com confecção de material por encomenda só deverá ser classificada como serviços de terceiros se o próprio órgão ou entidade fornecer a matéria-prima. Caso contrário, deverá ser classificada na natureza 449052, em se tratando de confecção de material permanente, ou na natureza 339030, se material de consumo.</li>
                                    <li>Art. 7º - Para a classificação das despesas de pequeno vulto, deverá ser utilizada a conta cuja função seja a mais adequada ao bem ou serviço.</li>
                            </ul>






                            







                            
                            
          
